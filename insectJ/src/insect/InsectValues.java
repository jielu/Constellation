package insect;

/**
 * This class will be generated during instrumentation and placed
 * within the instrumented program. Values stored in the generated
 * class will be used by InsECT during execution.
 *
 * @author <a href="mailto:anil@cc.gatech.edu">Anil Chawla</a>
 */
public class InsectValues {

  /* program name */
  public static String progName = null;
  
  /* serial code */
  public static String serial = null;  

  /* execution directory */
  public static String execDir = null;

  /* size of block hits file */
  public static int blockSize = 0;

  /* size of branch hits file */
  public static int branchSize = 0;

  /* determines if profiling is enabled */
  public static boolean profile = false;

}// InsectValues
